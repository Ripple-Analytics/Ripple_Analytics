%%%-------------------------------------------------------------------
%%% @doc Chaos Engineering Supervisor
%%%-------------------------------------------------------------------
-module(chaos_engineering_sup).
-behaviour(supervisor).

-export([start_link/0, init/1]).

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

init([]) ->
    SupFlags = #{strategy => one_for_one, intensity => 10, period => 60},
    ChildSpecs = [
        #{id => chaos_controller,
          start => {chaos_controller, start_link, []},
          restart => permanent,
          shutdown => 5000,
          type => worker},
        #{id => load_tester,
          start => {load_tester, start_link, []},
          restart => permanent,
          shutdown => 5000,
          type => worker}
    ],
    {ok, {SupFlags, ChildSpecs}}.
